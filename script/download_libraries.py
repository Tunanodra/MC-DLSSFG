base_url = "https://github.com/187J3X1-114514/superresolution/raw/refs/heads/assets"
import requests
import os
from pathlib import Path
import time
from concurrent.futures import ThreadPoolExecutor, as_completed
import threading

script_dir = Path(__file__).resolve().parent
parent_dir = script_dir.parent

# 全局统计变量
total_files = len(libraries)
completed_files = 0
failed_files = 0
total_downloaded = 0
start_time = time.time()
lock = threading.Lock()

def download_file(lib_desc):
    global completed_files, failed_files, total_downloaded

    lib_path = lib_desc[0]
    save_dir = parent_dir / Path(lib_desc[1]).parent
    save_dir.mkdir(parents=True, exist_ok=True)

    download_url = f"{base_url}/{lib_path}"
    local_path = save_dir / Path(lib_path).name
    counter = 0
    error = None
    while counter < 6:
        try:
            response = requests.get(download_url, stream=True, timeout=30)
            response.raise_for_status()

            with open(local_path, "wb") as f:
                for chunk in response.iter_content(chunk_size=8192):
                    f.write(chunk)
                    with lock:
                        total_downloaded += len(chunk)

            with lock:
                completed_files += 1
            return True, lib_path, None
        except Exception as e:
            time.sleep(1)
            error = e
            counter += 1
            continue
    with lock:
        failed_files += 1
        completed_files += 1
        return False, lib_path, str(error)

def update_progress():
    while completed_files < total_files:
        elapsed = time.time() - start_time
        speed = total_downloaded / elapsed if elapsed > 0 else 0
        progress = int((completed_files / total_files) * 100)

        print(f"\r进度: {progress}% ({completed_files}/{total_files}) | "
              f"速度: {speed/1024/1024:.2f} MB/s | "
              f"已下载: {total_downloaded/1024/1024:.2f} MB | "
              f"失败: {failed_files}", end="", flush=True)
        time.sleep(0.25)

progress_thread = threading.Thread(target=update_progress, daemon=True)
progress_thread.start()

with ThreadPoolExecutor(max_workers=8) as executor:
    futures = {executor.submit(download_file, lib_desc): lib_desc for lib_desc in libraries}

    for future in as_completed(futures):
        success, lib_path, error = future.result()
        if not success:
            print(f"\n下载失败: {lib_path} 错误: {error}")
            pass

progress_thread.join(timeout=1)

